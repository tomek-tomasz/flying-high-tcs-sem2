SELECT KATEGORIA AS "kategoria",
    COALESCE(ROUND(VAR_SAMP(CENA_NETTO),2),0.00) AS "wariancja", 
    COALESCE(ROUND(STDDEV_SAMP(CENA_NETTO),2),0.00) AS "odchylenie standardowe"
FROM PRODUKTY
GROUP BY KATEGORIA
ORDER BY 1,2,3;
