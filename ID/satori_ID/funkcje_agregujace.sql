--ZAD17
SELECT 
CASE
    WHEN NIP IS NULL THEN 'Brak'
ELSE
    'Podany'
END AS "NIP",
COUNT(*) AS "liczba klientów"
FROM KLIENCI
GROUP BY NIP IS NULL
ORDER BY 1,2;
