FROM CZYTELNICY C
FULL OUTER JOIN CZYTELNIK_ZAINTERESOWANIA CZ ON CZ.ID_CZYTELNIKA = C.ID_CZYTELNIKA
LEFT JOIN KATEGORIE KAT ON CZ.ID_KATEGORII = KAT.ID_KATEGORII
ORDER BY 1,2,3;
