--ZAD16
SELECT IMIE AS "imię", NAZWISKO,
COUNT(DISTINCT
    CASE
        WHEN ID_WYPOZYCZENIA IS NULL THEN NULL
        WHEN K.ID_KATEGORII IS NULL THEN -1
        ELSE K.ID_KATEGORII
    END
) AS "liczba różnych kategorii"
FROM CZYTELNICY C
LEFT JOIN WYPOZYCZENIA W ON C.ID_CZYTELNIKA = W.ID_CZYTELNIKA
LEFT JOIN EGZEMPLARZE E ON W.ID_EGZEMPLARZA = E.ID_EGZEMPLARZA
LEFT JOIN KSIAZKI K ON E.ID_KSIAZKI = K.ID_KSIAZKI
LEFT JOIN KATEGORIE KAT ON K.ID_KATEGORII = KAT.ID_KATEGORII
GROUP BY C.ID_CZYTELNIKA
ORDER BY 1,2,3;
