--ZAD2
SELECT NAZWA,
    COUNT(*)
    FROM KATEGORIE KAT
     JOIN KSIAZKI K ON K.ID_KATEGORII = KAT.ID_KATEGORII
GROUP BY NAZWA
ORDER BY 1,2; 
