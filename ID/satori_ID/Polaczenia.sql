--ZAD3
SELECT DISTINCT TYTUL, AUTOR
FROM KSIAZKI K
    NATURAL JOIN EGZEMPLARZE E
    NATURAL JOIN WYPOZYCZENIA W
WHERE DATA_ODDANIA IS NULL;
