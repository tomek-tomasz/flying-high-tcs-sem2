--ZAD5
SELECT C1.IMIE, C1.NAZWISKO, C2.IMIE, C2.NAZWISKO, C1.MIASTO
FROM CZYTELNICY C1
JOIN CZYTELNICY C2
ON C1.ID_CZYTELNIKA<C2.ID_CZYTELNIKA AND C1.MIASTO = C2.MIASTO
ORDER BY 1,2,3,4,5;
