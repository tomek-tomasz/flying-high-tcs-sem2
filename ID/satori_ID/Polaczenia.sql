--ZAD14
SELECT IMIE, NAZWISKO, 
COUNT(W.ID_WYPOZYCZENIA),
CASE
    WHEN COUNT(W.ID_WYPOZYCZENIA)>20 THEN 'mól książkowy'
    ELSE 'niedzielny czytelnik'
END
FROM CZYTELNICY C
JOIN WYPOZYCZENIA W ON C.ID_CZYTELNIKA = W.ID_CZYTELNIKA
WHERE DATA_WYPOZYCZENIA >= '2023-01-01'
GROUP BY C.ID_CZYTELNIKA
HAVING COUNT(W.ID_WYPOZYCZENIA)>=3
ORDER BY 3 DESC, 1, 2;
