--ZAD15
SELECT DISTINCT C.ID_CZYTELNIKA, 
IMIE, 
NAZWISKO, 
K.ID_KSIAZKI, 
TYTUL,
AUTOR,
CASE
    WHEN COUNT(ID_WYPOZYCZENIA)>0 THEN 'TAK'
    ELSE 'NIE'
END AS "wypożyczona"
FROM CZYTELNICY C
CROSS JOIN KSIAZKI K
LEFT JOIN EGZEMPLARZE E ON K.ID_KSIAZKI = E.ID_KSIAZKI
LEFT JOIN WYPOZYCZENIA W ON W.ID_EGZEMPLARZA = E.ID_EGZEMPLARZA 
AND C.ID_CZYTELNIKA = W.ID_CZYTELNIKA
GROUP BY 
    C.ID_CZYTELNIKA, 
    C.IMIE, 
    C.NAZWISKO, 
    K.ID_KSIAZKI, 
    K.TYTUL,
    K.AUTOR
ORDER BY 1;
