--ZAD10
SELECT NAZWA
FROM KATEGORIE KAT
JOIN KSIAZKI K ON KAT.ID_KATEGORII = K.ID_KATEGORII
JOIN EGZEMPLARZE E ON K.ID_KSIAZKI = E.ID_KSIAZKI
JOIN WYPOZYCZENIA W ON E.ID_EGZEMPLARZA = W.ID_EGZEMPLARZA
GROUP BY KAT.ID_KATEGORII
ORDER BY COUNT(ID_WYPOZYCZENIA) DESC, KAT.ID_KATEGORII
LIMIT 1
