--ZAD7
SELECT C.IMIE,
C.NAZWISKO,
COUNT(W.ID_WYPOZYCZENIA)
FROM CZYTELNICY C JOIN WYPOZYCZENIA W ON C.ID_CZYTELNIKA = W.ID_CZYTELNIKA
GROUP BY C.ID_CZYTELNIKA
HAVING SUM(CASE WHEN DATA_ODDANIA IS NULL THEN 0 ELSE 1 END) < COUNT(W.ID_WYPOZYCZENIA)
ORDER BY 1,2,3;
----
--ZAD8
SELECT  IMIE, NAZWISKO
FROM CZYTELNICY C JOIN WYPOZYCZENIA W ON C.ID_CZYTELNIKA = W.ID_CZYTELNIKA
WHERE (DATA_ODDANIA IS NOT NULL AND DATA_ODDANIA>OCZEKIWANA_DATA_ODDANIA) 
OR (DATA_ODDANIA IS NULL AND NOW() > OCZEKIWANA_DATA_ODDANIA)
GROUP BY C.ID_CZYTELNIKA, IMIE, NAZWISKO
ORDER BY 1,2;
----
--ZAD9
SELECT C.ID_CZYTELNIKA, IMIE, NAZWISKO, W.ID_WYPOZYCZENIA
FROM CZYTELNICY C JOIN WYPOZYCZENIA W ON C.ID_CZYTELNIKA = W.ID_CZYTELNIKA
WHERE W.ID_WYPOZYCZENIA & ID_WYPOZYCZENIA-1 = 0
ORDER BY 1,2,3,4;
