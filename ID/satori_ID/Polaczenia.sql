SELECT ID_EGZEMPLARZA,
       TYTUL
FROM EGZEMPLARZE E
    JOIN KSIAZKI K
        ON E.ID_KSIAZKI = K.ID_KSIAZKI
WHERE AUTOR = 'J.R.R. Tolkien'
ORDER BY 1,2;
