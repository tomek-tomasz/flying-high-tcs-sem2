--ZAD6
SELECT 
K.ID_KSIAZKI,
K.TYTUL,
CASE
WHEN COUNT(DISTINCT E.ID_EGZEMPLARZA)=
COUNT(
    CASE
        WHEN DATA_ODDANIA IS NULL AND W.ID_WYPOZYCZENIA IS NOT NULL THEN 1
        ELSE NULL 
    END
)
THEN FALSE
ELSE TRUE
END
FROM KSIAZKI K
LEFT JOIN EGZEMPLARZE E ON K.ID_KSIAZKI = E.ID_KSIAZKI
LEFT JOIN WYPOZYCZENIA W ON E.ID_EGZEMPLARZA = W.ID_EGZEMPLARZA
GROUP BY K.ID_KSIAZKI
ORDER BY 1,2,3;
