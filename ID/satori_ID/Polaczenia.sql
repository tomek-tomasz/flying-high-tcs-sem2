--ZAD4
SELECT AUTOR,
 SUM(CASE
        WHEN ID_EGZEMPLARZA IS NULL THEN 0 
        ELSE 1 
    END) AS "liczba egzemplarzy"
FROM KSIAZKI K LEFT JOIN EGZEMPLARZE E ON K.ID_KSIAZKI = E.ID_KSIAZKI
GROUP BY AUTOR
ORDER BY 1,2;
