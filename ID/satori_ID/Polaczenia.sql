--ZAD13
SELECT IMIE, NAZWISKO, TYTUL, AUTOR
FROM CZYTELNICY C
JOIN CZYTELNIK_ZAINTERESOWANIA CZ ON C.ID_CZYTELNIKA = CZ.ID_CZYTELNIKA
JOIN KATEGORIE KAT ON CZ.ID_KATEGORII = KAT.ID_KATEGORII
JOIN KSIAZKI K ON KAT.ID_KATEGORII = K.ID_KATEGORII
WHERE NAZWISKO LIKE 'K%'
ORDER BY 1,2,3,4;
