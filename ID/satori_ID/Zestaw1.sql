--ZAD18
SELECT 
    UPPER(LEFT(STANOWISKO,2)) || ID AS "wygenerowany kod", 
    ID AS "id strażnika", 
    NAZWISKO AS "nazwisko",
    STANOWISKO AS "stanowisko"
FROM STRAZNICY
ORDER BY 1,2,3,4; 
