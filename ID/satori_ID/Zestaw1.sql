--ZAD23
SELECT *
FROM STRAZNICY
ORDER BY 
    CASE
        WHEN SUBSTRING(PESEL FROM 3 FOR 1) = '2' THEN MAKE_DATE(('20'||LEFT(PESEL,2))::INT,  SUBSTRING(PESEL FROM 4 FOR 1)::INT, SUBSTRING(PESEL FROM 5 FOR 2)::INT)
        WHEN SUBSTRING(PESEL FROM 3 FOR 1) = '3' THEN MAKE_DATE(('20'||LEFT(PESEL,2))::INT,  ('1'||SUBSTRING(PESEL FROM 4 FOR 1))::INT, SUBSTRING(PESEL FROM 5 FOR 2)::INT)
    ELSE
        MAKE_DATE(('19'||LEFT(PESEL,2))::INT, SUBSTRING(PESEL FROM 3 FOR 2)::INT, SUBSTRING(PESEL FROM 5 FOR 2)::INT)
    END
LIMIT 5;
