--ZAD20
SELECT 
    IMIE || ' ' || NAZWISKO AS "imię i nazwisko",
    TO_CHAR(DATA_OD, 'Month') || ' , ' || TO_CHAR(DATA_OD,'DD') || ' ' || EXTRACT(YEAR FROM DATA_OD) AS "uzyskanie uprawnień"
FROM KIEROWCY
WHERE EXTRACT(YEAR FROM AGE(DATA_DO, DATA_OD)) < 5 AND DATA_DO IS NOT NULL
ORDER BY AGE(DATA_DO, DATA_OD);
